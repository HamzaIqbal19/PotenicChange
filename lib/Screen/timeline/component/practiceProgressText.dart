practiceProgressAfterText(String text) {
  if (text == '1' || text == '0') {
    return '“I felt low & irritated”';
  } else if (text == '2') {
    return '“I felt slightly down”';
  } else if (text == '3') {
    return '“I felt ok”';
  } else if (text == '4') {
    return '“I felt focused & good”';
  } else if (text == '5') {
    return '“I felt excited & good in myself”';
  }
}

practiceProgressBeforeText(String text) {
  if (text == '1'|| text == '0') {
    return '“I felt very low & demotivated”';
  } else if (text == '2') {
    return '“I felt slightly irritated, not fussed really"';
  } else if (text == '3') {
    return '“I felt ok”';
  } else if (text == '4') {
    return '“I felt motivated and ready to start”';
  } else if (text == '5') {
    return '“I felt great”';
  }
}
